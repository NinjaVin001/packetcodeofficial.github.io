<h2 style="text-align:center;">Labrador Retriever</h2>
<p> <h4>The most popular breed of dog in America is the adorable, sweet-faced Labrador Retriever. For a family searching for a medium-to-large dog, labs make sociable, outgoing, and affectionate companions with plenty of love to go around. Depending on the sex, the robust, well-balanced Labrador Retriever can stand 21.5 to 24.5 inches tall at the shoulder and weigh 55 to 80 pounds. The thick, durable coat is available in rich chocolate, black, and yellow. The large head, kind eyes, and thick, tapering "otter tail" seem to always be communicating the breed's intrinsic desire. Labs are known for being amiable. They get along well with the entire family and are amiable housemates. They also get along well with the canines and people of the neighborhood. </h4> </p>
