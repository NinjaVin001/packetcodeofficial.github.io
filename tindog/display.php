<h2 style="text-align:center;">Bulldog</h2>
<p> <h4>The Bulldog is a thick-set, low-slung, well-muscled bruiser whose "sourmug" face is the universal symbol of toughness and determination. He is kind but courageous, friendly but dignified. These submissive, devoted companions fit in both urban and rural settings.
A Bulldog cannot be confused with any other breed. The undershot jaw with dangling chops on either side, the small ears, the loose skin of the head, the furrowed brow, the pushed-in nose, and the unique rolling gait nearly shout "I'm a Bulldog!" The coat is brief, smooth, and glossy, and it comes in a variety of colors and designs. Although bulldogs can weigh up to 50 pounds, they will still attempt to snuggle up in your lap despite their size. </h4> </p>
<hr>
<p style="text-align:center;"><img src="https://cdn.britannica.com/78/232778-050-D3701AB1/English-bulldog-dog.jpg" alt="Logo"></p>

